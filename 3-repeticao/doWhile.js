let contador = 1;//A variável contador é inicializada com o valor 1.

do {
/* O bloco de código dentro do 'do' é executado, imprimindo o valor de contador
 * e incrementando-o em 1. 
 */
    console.log(contador);
    contador++;
  
  /* Após a execução do bloco, a condição contador <= 5 é avaliada.
   * Se for verdadeira, o loop se repete; caso contrário, ele termina.
   */  
} while (contador <= 5);

