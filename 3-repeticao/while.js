
let contador = 1; //A variável contador é inicializada com o valor 1
console.log('contando...')
/* O loop verifica se contador é menor ou igual a 5. 
 * Enquanto isso for verdade, o loop continuará.
 */
while (contador <= 5) {
    console.log(contador);
    contador++;//Após imprimir o valor, contador é incrementado em 1
    
} /* Quando contador se torna 6, a condição contador <= 5 é falsa,
   * então o loop termina.
   */
console.log('Terminei de contar')