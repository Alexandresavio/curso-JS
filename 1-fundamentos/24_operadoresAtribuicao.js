
//atribuição simples
const a = 7 
let b = 3

/**
 * Operadores de atribuição combinada, estes operadores realizam 
 * uma operação aritmética e atribuem o resultado à variável.
 */
b += a // forma abreviada da atribuição  b = b + a
console.log(b)

b -= 4 // forma abreviada da atribuição b = b - 4
console.log(b)

b *= 2 // forma abreviada da atribuição b = b * 2
console.log(b)

b /= 2 // forma abreviada da atribuição b = b / 2
console.log(b)

b %= 2 // forma abreviada da atribuição b = b % 2 -> resto da divisão
console.log(b)
